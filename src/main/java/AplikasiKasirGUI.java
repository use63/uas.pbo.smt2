    private void BbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BbayarActionPerformed
         // TODO add your handling code here:
        Double totalHarga, totalBayar, kembalian;
        totalHarga = Double.valueOf(TXTjumlahHarga.getText());
        totalBayar = Double.valueOf(TXTjumlahBayar.getText());
        kembalian = totalBayar - totalHarga;
        TXTjumlahKembalian.setText(String.valueOf(kembalian));
        
    }//GEN-LAST:event_BbayarActionPerformed

