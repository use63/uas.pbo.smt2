    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int batal = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin melanjutkan?", "Batal", JOptionPane.YES_NO_OPTION);
        if (batal == JOptionPane.YES_OPTION) {
            CBnamaMakanan.setSelectedIndex(0);
            TXThargaMakanan.setText("");
            TFjumlahBeli.setText("");
            TXTjumlahHarga.setText("");
            RBbungkus.setSelected(false);
            RBmakanDiSini.setSelected(false);
            TXTjumlahBayar.setText("");
            TXTjumlahKembalian.setText("");
            CBpph10.setSelected(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

