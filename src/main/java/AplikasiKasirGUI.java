    private void BhitungJumlahHargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BhitungJumlahHargaActionPerformed
        // TODO add your handling code here:
        Double harga, jumlah, hasil;
        harga = Double.valueOf(TXThargaMakanan.getText());
        jumlah = Double.valueOf(TFjumlahBeli.getText());
        hasil = harga*jumlah;
        TXTjumlahHarga.setText(String.valueOf(hasil));
    }//GEN-LAST:event_BhitungJumlahHargaActionPerformed

    private void CBnamaMakananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBnamaMakananActionPerformed
        // TODO add your handling code here:
        if (CBnamaMakanan.getSelectedItem() == "MI AYAM") {
            TXThargaMakanan.setText("10000");
        }else if(CBnamaMakanan.getSelectedItem() == "AYAM BAKAR") {
            TXThargaMakanan.setText("20000");
        }else if(CBnamaMakanan.getSelectedItem() == "NASI GORENG") {
            TXThargaMakanan.setText("12000");
        }else if(CBnamaMakanan.getSelectedItem() == "SATE AYAM") {
            TXThargaMakanan.setText("15000");
        }
    }//GEN-LAST:event_CBnamaMakananActionPerformed

    private void CBpph10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBpph10ActionPerformed
        // TODO add your handling code here:
        Double pph, kembalian;
        pph = Double.valueOf(TXTjumlahHarga.getText())/10;
        kembalian = Double.valueOf(TXTjumlahBayar.getText()) - Double.valueOf(TXTjumlahHarga.getText());
        kembalian = kembalian - pph;
        TXTjumlahKembalian.setText(String.valueOf(kembalian));
    }//GEN-LAST:event_CBpph10ActionPerformed
    
    
    private void RBbungkusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RBbungkusActionPerformed
        // TODO add your handling code here:
        Double harga, jumlah, hasil;
        harga = Double.valueOf(TXThargaMakanan.getText());
        jumlah = Double.valueOf(TFjumlahBeli.getText());
        hasil = (harga*jumlah)+2000;
        TXTjumlahHarga.setText(String.valueOf(hasil));
    }//GEN-LAST:event_RBbungkusActionPerformed

